                <img v-if="ruleForm.wenjutupian.substring(0,4)=='http'" class="upload-img" style="margin-right:20px;" v-bind:key="index" :src="ruleForm.wenjutupian.split(',')[0]" width="100" height="100">
                <img v-else class="upload-img" style="margin-right:20px;" v-bind:key="index" v-for="(item,index) in ruleForm.wenjutupian.split(',')" :src="baseUrl+item" width="100" height="100">
            </el-form-item>
