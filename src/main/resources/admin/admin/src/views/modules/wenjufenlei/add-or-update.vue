<script>
// 数字，邮件，手机，url，身份证校验
import { isNumber,isIntNumer,isEmail,isPhone, isMobile,isURL,checkIdCard } from "@/utils/validate";
import WenjuxinxiAddOrUpdate from "../wenjuxinxi/add-or-update";
export default {
	data() {
		let self = this
		var validateIdCard = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!checkIdCard(value)) {
				callback(new Error("请输入正确的身份证号码"));
			} else {
				callback();
			}
		};
		var validateUrl = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isURL(value)) {
				callback(new Error("请输入正确的URL地址"));
			} else {
				callback();
			}
		};
		var validateMobile = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isMobile(value)) {
				callback(new Error("请输入正确的手机号码"));
			} else {
				callback();
			}
		};
		var validatePhone = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isPhone(value)) {
				callback(new Error("请输入正确的电话号码"));
			} else {
				callback();
			}
		};
		var validateEmail = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isEmail(value)) {
				callback(new Error("请输入正确的邮箱地址"));
			} else {
				callback();
			}
		};
		var validateNumber = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isNumber(value)) {
				callback(new Error("请输入数字"));
			} else {
				callback();
			}
		};
		var validateIntNumber = (rule, value, callback) => {
			if(!value){
				callback();
			} else if (!isIntNumer(value)) {
				callback(new Error("请输入整数"));
			} else {
				callback();
			}
		};
		return {
			id: '',
			type: '',
			
			
			ro:{
				wenjufenlei : false,
			},
			
			
			ruleForm: {
				wenjufenlei: '',
			},
			
			wenjuxinxiDataList: [],
			wenjuxinxiLoading: false,
			wenjuxinxiDialogVisible: false,
			wenjuxinxiDialogParent: {
				showFlag: true,
				addOrUpdateFlag: true,
				wenjuxinxiCrossAddOrUpdateFlag: false,
				search: () => {
					this.loadWenjuxinxiList();
				},
				contentStyleChange: () => {},
				closeDialog: () => {
					this.wenjuxinxiDialogVisible = false;
					this.loadWenjuxinxiList();
				}
			},
		
			rules: {
				wenjufenlei: [
				],
			}
		};
	},
	props: ["parent"],
	computed: {



	},
    components: {
		WenjuxinxiAddOrUpdate,
    },
	created() {
	},
	methods: {
		
		// 下载
		download(file){
			window.open(`${file}`)
		},
		// 初始化
		init(id,type) {
			this.wenjuxinxiDataList = [];
			if (id) {
				this.id = id;
				this.type = type;
			}
			if(this.type=='info'||this.type=='else'){
				this.info(id);
			}else if(this.type=='logistics'){
				this.logistics=false;
				this.info(id);
			}else if(this.type=='cross'){
				var obj = this.$storage.getObj('crossObj');
				for (var o in obj){
						if(o=='wenjufenlei'){
							this.ruleForm.wenjufenlei = obj[o];
							this.ro.wenjufenlei = true;
							continue;
						}
				}
				

			}
			
			
			// 获取用户信息
			this.$http({
				url: `${this.$storage.get('sessionTable')}/session`,
				method: "get"
			}).then(({ data }) => {
				if (data && data.code === 0) {
					
					var json = data.data;
				} else {
					this.$message.error(data.msg);
				}
			});
			
			
		},
    // 多级联动参数

    info(id) {
      this.$http({
        url: `wenjufenlei/info/${id}`,
        method: "get"
      }).then(({ data }) => {
        if (data && data.code === 0) {
        this.ruleForm = data.data;
        this.loadWenjuxinxiList();
        //解决前台上传图片后台不显示的问题
        let reg=new RegExp('../../../upload','g')//g代表全部
        } else {
          this.$message.error(data.msg);
        }
      });
    },

	loadWenjuxinxiList() {
		if (!this.ruleForm.wenjufenlei) {
			this.wenjuxinxiDataList = [];
			return;
		}
		this.wenjuxinxiLoading = true;
		this.$http({
			url: "wenjuxinxi/page",
			method: "get",
			params: {
				page: 1,
				limit: 999,
				sort: 'id',
				order: 'desc',
				wenjufenlei: this.ruleForm.wenjufenlei
			}
		}).then(({ data }) => {
			if (data && data.code === 0) {
				this.wenjuxinxiDataList = data.data.list || [];
			} else {
				this.wenjuxinxiDataList = [];
				this.$message.error(data.msg);
			}
		}).finally(() => {
			this.wenjuxinxiLoading = false;
		});
	},

	openWenjuxinxiDetail(id) {
		this.wenjuxinxiDialogVisible = true;
		this.$nextTick(() => {
			this.$refs.wenjuxinxiAddOrUpdate.init(id, 'info');
		});
	},

	openWenjuxinxiEdit(id) {
		this.wenjuxinxiDialogVisible = true;
		this.$nextTick(() => {
			this.$refs.wenjuxinxiAddOrUpdate.init(id, 'else');
		});
	},

	deleteWenjuxinxi(id) {
		this.$confirm(`确定删除该文具商品吗?`, "提示", {
			confirmButtonText: "确定",
			cancelButtonText: "取消",
			type: "warning"
		}).then(() => {
			this.$http({
				url: "wenjuxinxi/delete",
				method: "post",
				data: [Number(id)]
			}).then(({ data }) => {
				if (data && data.code === 0) {
					this.$message.success("删除成功");
					this.loadWenjuxinxiList();
				} else {
					this.$message.error(data.msg);
				}
			});
		});
	},


    // 提交
    onSubmit() {



var objcross = this.$storage.getObj('crossObj');

      //更新跨表属性
       var crossuserid;
       var crossrefid;
       var crossoptnum;
       if(this.type=='cross'){
                var statusColumnName = this.$storage.get('statusColumnName');
                var statusColumnValue = this.$storage.get('statusColumnValue');
                if(statusColumnName!='') {
                        var obj = this.$storage.getObj('crossObj');
                       if(statusColumnName && !statusColumnName.startsWith("[")) {
                               for (var o in obj){
                                 if(o==statusColumnName){
                                   obj[o] = statusColumnValue;
                                 }
                               }
                               var table = this.$storage.get('crossTable');
                             this.$http({
                                 url: `${table}/update`,
                                 method: "post",
                                 data: obj
                               }).then(({ data }) => {});
                       } else {
                               crossuserid=this.$storage.get('userid');
                               crossrefid=obj['id'];
                               crossoptnum=this.$storage.get('statusColumnName');
                               crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                        }
                }
        }
       this.$refs["ruleForm"].validate(valid => {
         if (valid) {
		 if(crossrefid && crossuserid) {
			 this.ruleForm.crossuserid = crossuserid;
			 this.ruleForm.crossrefid = crossrefid;
			let params = { 
				page: 1, 
				limit: 10, 
				crossuserid:this.ruleForm.crossuserid,
				crossrefid:this.ruleForm.crossrefid,
			} 
			this.$http({ 
				url: "wenjufenlei/page", 
				method: "get", 
				params: params 
			}).then(({ 
				data 
			}) => { 
				if (data && data.code === 0) { 
				       if(data.data.total>=crossoptnum) {
					     this.$message.error(this.$storage.get('tips'));
					       return false;
				       } else {
					 this.$http({
					   url: `wenjufenlei/${!this.ruleForm.id ? "save" : "update"}`,
					   method: "post",
					   data: this.ruleForm
					 }).then(({ data }) => {
					   if (data && data.code === 0) {
					     this.$message({
					       message: "操作成功",
					       type: "success",
					       duration: 1500,
					       onClose: () => {
						 this.parent.showFlag = true;
						 this.parent.addOrUpdateFlag = false;
						 this.parent.wenjufenleiCrossAddOrUpdateFlag = false;
						 this.parent.search();
						 this.parent.contentStyleChange();
					       }
					     });
					   } else {
					     this.$message.error(data.msg);
					   }
					 });

				       }
				} else { 
				} 
			});
		 } else {
			 this.$http({
			   url: `wenjufenlei/${!this.ruleForm.id ? "save" : "update"}`,
			   method: "post",
			   data: this.ruleForm
			 }).then(({ data }) => {
			   if (data && data.code === 0) {
			     this.$message({
			       message: "操作成功",
			       type: "success",
			       duration: 1500,
			       onClose: () => {
				 this.parent.showFlag = true;
				 this.parent.addOrUpdateFlag = false;
				 this.parent.wenjufenleiCrossAddOrUpdateFlag = false;
				 this.parent.search();
				 this.parent.contentStyleChange();
			       }
			     });
			   } else {
			     this.$message.error(data.msg);
			   }
			 });
		 }
         }
       });
    },
    // 获取uuid
    getUUID () {
      return new Date().getTime();
    },
    // 返回
    back() {
      this.parent.showFlag = true;
      this.parent.addOrUpdateFlag = false;
      this.parent.wenjufenleiCrossAddOrUpdateFlag = false;
      this.parent.contentStyleChange();
    },
  }
};
</script>
