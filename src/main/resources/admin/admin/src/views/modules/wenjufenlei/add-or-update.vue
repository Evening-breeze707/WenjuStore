							<el-button type="success" size="mini" @click="openWenjuxinxiDetail(scope.row.id)">详情</el-button>
							<el-button type="primary" size="mini" @click="openWenjuxinxiEdit(scope.row.id)">修改</el-button>
							<el-button type="danger" size="mini" @click="deleteWenjuxinxi(scope.row.id)">删除</el-button>
